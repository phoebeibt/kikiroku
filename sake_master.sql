CREATE TABLE IF NOT EXISTS sake_areas (
  id   INTEGER PRIMARY KEY,
  name TEXT NOT NULL
);

CREATE TABLE IF NOT EXISTS sake_breweries (
  id      INTEGER PRIMARY KEY,
  name    TEXT NOT NULL,
  area_id INTEGER REFERENCES sake_areas(id)
);

CREATE TABLE IF NOT EXISTS sake_brands (
  id         INTEGER PRIMARY KEY,
  name       TEXT NOT NULL,
  brewery_id INTEGER REFERENCES sake_breweries(id)
);

CREATE TABLE IF NOT EXISTS sake_rice (
  id   SERIAL PRIMARY KEY,
  name TEXT NOT NULL UNIQUE
);

ALTER TABLE sake_areas     ENABLE ROW LEVEL SECURITY;
ALTER TABLE sake_breweries ENABLE ROW LEVEL SECURITY;
ALTER TABLE sake_brands    ENABLE ROW LEVEL SECURITY;
ALTER TABLE sake_rice      ENABLE ROW LEVEL SECURITY;

CREATE POLICY IF NOT EXISTS "public read" ON sake_areas     FOR SELECT USING (true);
CREATE POLICY IF NOT EXISTS "public read" ON sake_breweries FOR SELECT USING (true);
CREATE POLICY IF NOT EXISTS "public read" ON sake_brands    FOR SELECT USING (true);
CREATE POLICY IF NOT EXISTS "public read" ON sake_rice      FOR SELECT USING (true);


