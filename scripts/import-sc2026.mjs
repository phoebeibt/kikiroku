async function main() {
  const rows = DATA.map(([brewery_name, brand_name, prefecture, medal]) => ({
    year: 2026,
    year_code: 'SC_2026',
    brewery_name,
    brand_name,
    prefecture,
    is_gold: medal === 'GOLD',
    corp_number: null,
  }))

  console.log(`Inserting ${rows.length} rows...`)

  // Insert in batches of 50
  let inserted = 0
  for (let i = 0; i < rows.length; i += 50) {
    const batch = rows.slice(i, i + 50)
    const { data, error } = await db.from('sake_awards').insert(batch).select('id')
    if (error) {
      console.error(`✗ Batch ${i}–${i + batch.length}: ${error.message}`)
      process.exit(1)
    }
    inserted += data.length
    console.log(`  ✓ ${inserted}/${rows.length}`)
  }

  console.log(`\n✅  Inserted ${inserted} SC_2026 records`)

  const gold = DATA.filter(r => r[3] === 'GOLD').length
  const silver = DATA.filter(r => r[3] === 'SILVER').length
  const bronze = DATA.filter(r => r[3] === 'BRONZE').length
  console.log(`   GOLD: ${gold}  SILVER: ${silver}  BRONZE: ${bronze}`)
}

main().catch(e => { console.error(e); process.exit(1) })
